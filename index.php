    <h2 class="blog-post-title" align="center">Company Information</h2>
    <p>OmniBase Services of Texas maintains and administers the central database for the cities and counties contracted to use the Department of Public Safety's Failure to Appear Program. The FTA program as authorized by Chapter 706 of the Texas Transportation Code, provides an effective collection and enforcement tool by restricting the violator's ability to renew their drivers license for outstanding violations. OmniBase Services of Texas is VID # 176047826601 in the Catalog Information Systems Vendor Program (CISV).
    </p>
    <h2 align="center">Experience</h2>
    <p> The Failure to Appear Program was authorized by the Texas Legislature in 1995 and the Texas Department of Public Safety was assigned responsibility for the administration of the program. OmniBase responded to the Request for Proposal and was selected by TDPS as the initial vendor for the program in 1996. Accordingly, OmniBase is the only company to administer the FTA program and has been included in each of the enhancements to the program since it was first authorized. </p>
    <h2 align="center">Commitment</h2>
    <p>OmniBase is committed to building effective two-way relationships with political subdivisions through its computer information systems and staff. The computer technical staff offers first-rate response time to technical questions, and the entire staff offers a commitment for a positive relationship between governmental entities and our company.
    </p>
    <h2 align="center">What We Provide</h2>
    <ul>
      <li> Customized software for database entry free of charge to jurisdictions contracting with TDPS for the FTA program </li>
      <li> Automated central database of violators reported by jurisdictions to the program </li>
      <li> Mail letter of notification to violators after TDPS acceptance advising of restriction on drivers license and providing information on contacting the reporting jurisdiction </li>
      <li> 800 number and operator assistance for offenders to contact our Interactive Voice Response System (IVR). System is available on a 24-hour basis to program-affected citizens with a means to call our office at any time to access a personal file on them concerning their violations </li>
      <li> Maintain database on violators for five years after clearance and indefinitely for non-clearances </li>
      <li> Training for jurisdictions using Failure to Appear software </li>
    </ul>
<h2 align="center">How We Operate</h2>
    <ul>
      <li> City/county transmits new offenses to Failure to Appear Database </li>
      <li> OmniBase notifies TDPS to flag a violator’s driver license record </li>
      <li> OmniBase notifies violator of restriction upon renewal of drivers license view mail </li>
      <li> OmniBase assists violators in resolving open offenses </li>
      <li> Violator contacts city/county and resolves open offenses </li>
      <li> City/county collects fine, court costs, and the statutory administrative fee </li>
      <li> City/county notifies OmniBase Services of disposition of offense </li>
      <li> OmniBase Services notifies TDPS to remove the restriction on drivers license </li>
      <li> City/county keeps any fines collected and portion of the administrative fee </li>
    </ul>
