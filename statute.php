              <h3>Payment of Administrative Fee
              </h3>
              <ol type="A">
                <li>                A person who fails to appear for a complaint or citation for an offense described by Section 706.002 (a) shall be required to pay an administrative fee of $30 for each violation for which the person failed to appear, unless the person is acquitted of the charges for which the person failed to appear. The person shall pay the fee when:
                  <ol>
                    <li>the court enters judgement on the underlying offense reported to the department;</li>
                    <li> the underlying offense is dismissed; or
                    </li>
                    <li>bond or other security is posted to reinstate the charge for which the warrant was issued.                    </li>
                  </ol>
                </li>
                <li> A person who fails to pay or satisfy a judgement ordering the payment of a fine and cost in the manner the court orders shall be required to pay an administrative fee of $30.
                </li>
                <li>The department may deny renewal of the drivers license of a person who does not pay a fee due under this section until the fee is paid. The fee required by this section is in addition to any other fee required by law.
                </li>
    </ol>
