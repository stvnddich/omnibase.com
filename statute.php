<h3>Contract With Department    </h3>
  <ol type="A">
    <li>A political subdivision may contract with the department to provide information necessary for the department to deny renewal of the driver's license of a person who fails to appear for a complaint or citation or fails to pay or satisfy a judgement ordering payment of a fine and cost in the manner ordered by the court in a matter involving any offense that a court has jurisdiction of under Chapter 4, Code of Criminal Procedure.      </li>
    <li>A contract under this section:
      <ol>
        <li>must be made in accordance with Chapter 791, Government Code; and          </li>
        <li>is subject to the ability of the parties to provide or pay for the services required under the contract.</li></ol></li></ol>
