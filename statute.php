<h3>Warning; Citation          </h3>
          <ol type="A">
            <li>If a political subdivision has contracted with the department, a peace officer authorized to issue a citation in the jurisdiction of the political subdivision shall issue a written warning to each person to whom the officer issues a citation for a violation of a traffic law in the jurisdiction of the political subdivision.            </li>
            <li>The warning under Subsection a:
              <ol>
                <li>is in addition to any other warning required by law;                </li>
                <li>must state in substance that if the person fails to appear in court as provided by law for the prosecution of the offense or if the person fails to pay or satisfy a judgement ordering the payment of a fine and cost in the manner ordered by the court, the person may be denied renewal of the person's driver's license; and                </li>
                <li>may be printed on the same instrument as the citation.                </li>
              </ol></li></ol>
