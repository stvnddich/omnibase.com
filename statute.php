<a name="5"></a>
    <h3>Clearance Notice to Department    </h3>
    <ol type="A">
      <li>A political subdivision shall notify the department that there is no cause to continue to deny renewal of a person's driver's license based on the person's previous failure to appear or failure to pay or satisfy a judgement ordering the payment of a fine and cost in the manner ordered by the court in a matter involving an offense described by Section 706.002(a), on payment of a fee as provided by Section 706.006 and:
        <ol>
          <li>the perfection of an appeal of the case for which the warrant of arrest was issued or judgement arose;          </li>
          <li>the dismissal of the charge for which the warrant of arrest was issued or judgement arose;</li>
          <li> the posting of bond or the giving of other security to reinstate the charge for which the warrant was issued;          </li>
          <li>the payment or discharge of the fine and cost owed on an outstanding judgement of the court; or          </li>
          <li>other suitable arrangements to pay the fine and cost within the court's discretion</li>
        </ol>
      <li>The department may not continue to deny the renewal of the person's drivers license under this chapter after the department receives notice:
        <ol>
          <li>under Subsection a;
          </li>
          <li>that the person was acquitted of the charge on which the person failed to appear; or
          </li>
          <li>from the political subdivision that the failure to appear report or court order to pay a fine or cost relating to the person:
            <ol type="a">
              <li>was sent to the department in error; or
              </li>
              <li>has been destroyed in accordance with the political subdivision's records retention policy.
              </li>
            </ol></li></ol></li></li></ol>
