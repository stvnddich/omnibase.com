<h3>Definitions</h3>
  <ul>
    <li>
      "Complaint" means a notice of an offense as described by Article 27.14(d) or 45.019, Code of Criminal Procedure.      </li>
    <li>"Department" means the Department of Public Safety.      </li>
    <li>"Driver's license" has the meaning assigned by Section 521.001.      </li>
    <li>"Highway or street" has the meaning assigned by Section 541.302.      </li>
    <li>"Motor vehicle" has the meaning assigned by Section 541.201.      </li>
    <li>"Operator" has the meaning assigned by Section 541.001.</li>
    <li> "Political subdivision" means a municipality or county.</li>
    <li> "Public place" has the meaning assigned by Section 1.07, Penal Code.</li>
    <li> "Traffic law" means a statute or ordinance, a violation of which is a misdemeanor punishable by a fine in an amount not to exceed $1,000, that:
      <ol type="a">
        <li>regulates an operator's conduct or condition while operating a motor vehicle on a highway or street or in a public place;          </li>
        <li>regulates the condition of a motor vehicle while it is being operated on a highway or street;
          relates to the driver's license status of an operator while operating a motor vehicle on a highway or street; </li>
        <li>or
          relates to the registration status of a motor vehicle while it is being operated on a highway or street.</li>
        </ol>
    </li>
    </ul>
