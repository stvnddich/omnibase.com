<a name="7"></a>
              <h3>Records Relating to Fees; Disposition of Fees
              </h3>
              <ol type="A">
                <li>An officer collecting a fee under Section 706.006 shall
                  <ol>
                    <li>keep separate records of the money; and
                    </li>
                    <li>deposit the money in the appropriate municipal or county treasury.
                    </li>
                  </ol>
                </li>
                <li>The custodian of the municipal or county treasury may:
                  <ol>
                    <li>deposit each fee collected under Section 706.006 in an interest-bearing account; and
                    </li>
                    <li>retain for the municipality or county the interest earned on money in the account.
                    </li>
                  </ol>
                </li>
                <li>The custodian shall keep records of money received and disbursed under this section and shall provide an annual report, in the form approved by the comptroller, of all money received and disbursed under this section to:
                  <ol>
                    <li>the comptroller;
                    </li>
                    <li>the department; and
                    </li>
                    <li>another entity as provided by interlocal contract.
                    </li>
                  </ol>
                </li>
                <li>Of each fee collected under Section 706.006, the custodian of a municipal or county treasury shall:
                  <ol>
                    <li>send $20 to the comptroller on or before the last day of each calendar quarter; and
                    </li>
                    <li>deposit the remainder to the credit of the general fund of the municipality or county.</li>
                  </ol>
                </li>
                <li> Of each $20 received by the comptroller, the comptroller shall deposit $10 to the credit of the department to implement this chapter.</li>
    </ol>
