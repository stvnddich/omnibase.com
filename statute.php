<a name="4"></a>

                <h3>Denial of Renewal of Driver's License                </h3>
                <ol type="A">
                  <li>If a political subdivision has contracted with the department, on receiving the necessary information from the political subdivision the department may deny renewal of the person's driver's license for failure to appear based on a complaint or citation or failure to pay or satisfy a judgement ordering the payment of a fine and cost in the manner ordered by the court in a matter involving an offense described by Section 706.002 a.                  </li>
                  <li>The information must include
                  \
                    <ol>
                      <li>the name, date of birth, and driver's license number of the person;                      </li>
                      <li>the nature and date of the alleged violation;                      </li>
                      <li>a statement that the person failed to appear as required by law or failed to satisfy a judgement ordering the payment of a fine and cost in the manner ordered by the court in a matter involving an offense described by Section 706.002 (a); and                      </li>
                      <li>any other information required by the department.                      </li></ol></li></ol>
