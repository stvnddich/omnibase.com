              </h3>
              <ol type="A">
                <li>The department may contract with a private vendor to implement this chapter.
                </li>
                <li>The vendor performing the contract may be compensated by each political subdivision that has contracted with the department.
                </li>
                <li>Except for an action based on a citation issued by a peace officer employed by the department, the vendor may not be compensated with state money.</li>
    </ol>
